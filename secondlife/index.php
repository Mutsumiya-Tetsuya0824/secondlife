    function addToCart(symbol, label, cart) {
      cart.push({
        symbol: symbol,
        label: label,
        date: Math.floor(new Date / 1E3)
      });
    }

    function setCart(cart, cookieName, formId) {
      // JavaScriptオブジェクトをJSON文字列に変換
      var jsonData = JSON.stringify(cart);
      // URLエンコード
      var encodedData = encodeURIComponent(jsonData);
      // クッキーの有効期限設定（7日間）
      var expires = new Date();
      expires.setDate(expires.getDate() + 7);
      var expiresStr = expires.toUTCString();
      // クッキーに保存
      document.cookie = cookieName + "=" + encodedData + "; expires=" + expiresStr + "; path=/inquiry/form/catalog";
      // ページ遷移
      var form = document.getElementById(formId);
      form.action = "/inquiry/form/catalog/?page=input";
      form.submit();
    }

    function deleteCookie(cookieName, path) {
      // クッキーの削除
      document.cookie = cookieName + '=; expires=Thu, 01 Jan 1970 00:00:00 UTC; path=' + path;
